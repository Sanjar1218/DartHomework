// Return the days of the week, return the days of the week according to the numbers 1 to 7.
//Use the elif statments. 1: "Monday" 2: "Tuesday" 3: "Wednesday" 4: "Thursday" 5: "Friday" 6: "Saturday" 7: "Sunday"

void main() {
  int n = 3;
  if (n == 1) {
    print("Monday");
  } else if (n == 2) {
    print("Tuesday");
  } else if (n == 3) {
    print("Wednesday");
  } else if (n == 4) {
    print("Thursday");
  } else if (n == 5) {
    print("Friday");
  } else if (n == 6) {
    print("Saturday");
  } else if (n == 7) {
    print("Sunday");
  }
}
